// Create a function: Takes 2 arguments, Multiplys them , returns result

// Input: Number
// Output: Number

function multiply(number1, number2) {
  return number1 * number2;
}

console.log(multiply(5, 3) === 15); // logs true
console.log(multiply(5, 12) === 60); // logs true
console.log(multiply(5, 4) === 20); // logs true
console.log(multiply(6, 3) === 18); // logs true
console.log(multiply(1, 3) === 3); // logs true