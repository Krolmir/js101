// Initialize a new string
// Itterate over the string with split and foreach
// check if the word is a match if so out put with ***word.upcase*** format
// else return word
// join with ' '

function searchWord(word, string) {
  let result = [];
  string.split(' ').forEach(miniString => {
    if (word.toLowerCase() === miniString.toLowerCase()) {
      result.push(`***${miniString.toUpperCase()}***`);
    } else {
      result.push(miniString);
    }
  });

  return result.join(' ');
}

